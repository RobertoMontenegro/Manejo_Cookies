var map, infoWindow, geocoder;
var markers=[]; 

//Se crea el mapa con los datos que ya fueron guardados en cookies
function initMap() {
  //Busca en las cookies la última dirección que buscaste. Si no está, por default se centra en Monterrey.
  let lastSearchedLocation = getCookie("LastSearchedLocation");
  (lastSearchedLocation == "")
    ? lastSearchedLocation = { lat: 25.6714, lng: -100.309 }  
    : lastSearchedLocation = JSON.parse(lastSearchedLocation);

  //Busca en las cookies las posiciones de los marcadores que agregaste al mapa. Si no hay no se despliega nada en pantalla
  let markersfound = getCookie("MarkersLocation");
  (markersfound == "")
    ? markersfound = []
    : markersfound = JSON.parse(markersfound);
  
  //Busca tu estilo preferido en las cookies. Si no está se usa el estilo por default.
  let preferedStyle = getCookie("Style");
  (preferedStyle == "")
    ? preferedStyle = []
    : preferedStyle = mapThemes[preferedStyle];

  //Se crea el mapa
  map = new google.maps.Map(document.getElementById("map"), {
    center: lastSearchedLocation,
    zoom: 13,
    styles: preferedStyle,
    mapTypeControlOptions: {
      mapTypeIds: ["roadmap", "satellite", "hybrid", "terrain", "styled_map"],
    }
  });

  //Cada marcador en el array de marcadores se busca en las cookies para aplicarlos al mapa
  markersfound.forEach(m => {
    placeMarker(m, map);    
  });
 
  //Al darle click izquierdo en el mapa se crea un marcador en ese punto
  map.addListener("click", (e) => {
    placeMarker(e.latLng, map);
  });

  //Se manda a llamar la función para agregar la geolocalización
  addGeolocationFeature();
}

//función para crear un marcador y personalizar sus comportamientos
function placeMarker(latLng, map) {
  //Se crea el marcador, luego se agrega al array de marcadores
  let marker = new google.maps.Marker({
    map: map, 
    position: latLng,
    title: '',
    draggable: true,
    animation: google.maps.Animation.BOUNCE
  });
  markers.push(marker);
  saveCookie("MarkersLocation", JSON.stringify(markers.map((m) => m.getPosition())), 30);
  marker.setTitle(marker.getPosition().toString());

  //Al empezar a arrastrar un marcador se borra la anterior posición en las cookies
  marker.addListener('dragstart', () => {
    markers.splice(markers.indexOf(marker),1);
    saveCookie("MarkersLocation", JSON.stringify(markers.map((m) => m.getPosition())), 30);
  });

  //Al terminar de arrastrar un marcador se guarda la nueva posición en las cookies
  marker.addListener('dragend', () => {
    map.setCenter(marker.getPosition());
    marker.setTitle(marker.getPosition().toString());
    markers.push(marker);
    saveCookie("MarkersLocation", JSON.stringify(markers.map((m) => m.getPosition())), 30);
  });

  //Al darle click derecho a un marcador se elimina
  marker.addListener('rightclick', () => {
    marker.setMap(null);
    markers.splice(markers.indexOf(marker),1);
    saveCookie("MarkersLocation", JSON.stringify(markers.map((m) => m.getPosition())), 30);
  });
}

//Se toma el texto escrito en el input para tratar de buscar la dirección ingresada y centrar el mapa en ese punto
function searchDirection() {
  geocoder = new google.maps.Geocoder();
  const inputDirection = document.getElementById('input-direction');
  this.geocoder.geocode({'address': inputDirection.value}, (results, status) => {
    if (status == 'OK') { 
      map.setCenter(results[0].geometry.location);
      map.setZoom(12);
      saveCookie("LastSearchedLocation", JSON.stringify(results[0].geometry.location), 30);
      console.log(results);
    } else {
      alert('Geocode no funcionó exitosamente... ' + status);
    }
  });
}


//Se agrega la funcionalidad para encontrar la ubicación de tu dispositivo cuando presionas el botón "Ir a mi ubicación actual"
function addGeolocationFeature()
{
  infoWindow = new google.maps.InfoWindow();
  const locationButton = document.createElement("button");
  locationButton.textContent = "Ir a mi ubicación actual";
  locationButton.classList.add("custom-map-control-button");
  map.controls[google.maps.ControlPosition.TOP_CENTER].push(locationButton);
  locationButton.addEventListener("click", () => {
    if (navigator.geolocation) {
      navigator.geolocation.getCurrentPosition(
        (position) => { 
          const pos = {
            lat: position.coords.latitude,
            lng: position.coords.longitude,
          };
          infoWindow.setPosition(pos);
          infoWindow.setContent("Usted está aquí");
          infoWindow.open(map);
          map.setCenter(pos);
        },
        () => {
          handleLocationError(true, infoWindow, map.getCenter());
        }
      );
    } else {
      handleLocationError(false, infoWindow, map.getCenter());
    }
  });
}


//Mensaje de error en caso de que la geolocalización falle
function handleLocationError(browserHasGeolocation, infoWindow, pos) {
  infoWindow.setPosition(pos);
  infoWindow.setContent(
    browserHasGeolocation
      ? "Error: La Geolocalización fallo."
      : "Error: Tu navegador no soporta geolocalización."
  );
  infoWindow.open(map);
}

//Se cambia el tema y se guarda en las cookies
function changeTheme() {
  var selectedTheme = document.getElementById("theme-select");
  const styledMapType = new google.maps.StyledMapType(
    mapThemes[selectedTheme.value],
    { name: "Styled Map"}
  );
  map.mapTypes.set("styled_map", styledMapType);
  map.setMapTypeId("styled_map");
  saveCookie("Style", selectedTheme.value, 30);
}

//Se guarda una cookie según los parámetros de nombre, contenido y en cuantos días expira la cookie
function saveCookie(cookieName, cookieContent, expirationDays) {
  let date = new Date();
  date.setTime(date.getTime() + (expirationDays*24*60*60*1000));
  let expires = "expires="+ date.toUTCString();
  document.cookie = cookieName + "=" + cookieContent + ";" + expires + ";path=/";
}

//Obtiene la cookie que tenga el nombre pasado por parámetro
function getCookie(cookieName) {
  var name = cookieName + "=";
  var decodedCookie = decodeURIComponent(document.cookie);
  var ca = decodedCookie.split(';');
  for(var i = 0; i <ca.length; i++) {
    var c = ca[i];
    while (c.charAt(0) == ' ') {
      c = c.substring(1);
    }
    if (c.indexOf(name) == 0) {
      return c.substring(name.length, c.length);
    }
  }
  return "";
}

